package com.fise.model.dto;



public class AuthDirInfo {
	
	private int AuthDirId;
	private String SubFunName;
	private int SubFunId;
	
	
	public int getAuthDirId() {
		return AuthDirId;
	}
	public void setAuthDirId(int authDirId) {
		AuthDirId = authDirId;
	}
	public String getSubFunName() {
		return SubFunName;
	}
	public void setSubFunName(String subFunName) {
		SubFunName = subFunName;
	}
	public int getSubFunId() {
		return SubFunId;
	}
	public void setSubFunId(int subFunId) {
		SubFunId = subFunId;
	}
	       	
}
