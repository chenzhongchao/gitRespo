package com.fise.model.param;

public class IMCompany {
	private String UserName;
	private String AuthenticCode;
	private int CompanyId;
	private String CompanyName;
	private String CompanyAddr;
	private String CompanyDescribe;
	private String CompanyContacts;
	private String CompanyPhone;
	private String Email;
	private int[] AuthenticId;
	
	
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getAuthenticCode() {
		return AuthenticCode;
	}
	public void setAuthenticCode(String authenticCode) {
		AuthenticCode = authenticCode;
	}
	public int getCompanyId() {
		return CompanyId;
	}
	public void setCompanyId(int companyId) {
		CompanyId = companyId;
	}
	public String getCompanyName() {
		return CompanyName;
	}
	public void setCompanyName(String companyName) {
		CompanyName = companyName;
	}
	public String getCompanyAddr() {
		return CompanyAddr;
	}
	public void setCompanyAddr(String companyAddr) {
		CompanyAddr = companyAddr;
	}
	public String getCompanyDescribe() {
		return CompanyDescribe;
	}
	public void setCompanyDescribe(String companyDescribe) {
		CompanyDescribe = companyDescribe;
	}
	public String getCompanyContacts() {
		return CompanyContacts;
	}
	public void setCompanyContacts(String companyContacts) {
		CompanyContacts = companyContacts;
	}
	public String getCompanyPhone() {
		return CompanyPhone;
	}
	public void setCompanyPhone(String companyPhone) {
		CompanyPhone = companyPhone;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public int[] getAuthenticId() {
		return AuthenticId;
	}
	public void setAuthenticId(int[] authenticId) {
		AuthenticId = authenticId;
	}
	
	
}
